def is_django_migration(path: Path) -> bool:
    try:
        src = path.read_text()
        return "class Migration" in src and "django.db" in src
    except Exception:
        return False


def generate_django_fix(migration_path: str) -> DjangoFixSuggestion | None:
    """
    Analyze a Django migration file and return a fix suggestion.

    Structural fixes (high/medium confidence, no data movement):
      RunSQL without reverse_sql   -> reverse_sql=migrations.RunSQL.noop
      RunPython without reverse_code -> reverse_code=migrations.RunPython.noop

    Data-loss fixes (medium confidence, fully functional backup/restore):
      RemoveField -> RunPython(backup, restore) injected before the op
      DeleteModel -> RunPython(backup, restore) injected before the op

    Generated code stores backup data in a shared _mrt_backups table using
    keyset-paginated chunked writes.  See module docstring for limitations.

    Returns None if no fix is needed.
    """
    path = Path(migration_path)
    source = path.read_text()

    try:
        tree = ast.parse(source)
    except SyntaxError as e:
        raise ValueError(f"Cannot parse {path.name}: {e}") from e

    migration_class: ast.ClassDef | None = None
    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef) and node.name == "Migration":
            migration_class = node
            break
    if migration_class is None:
        return None

    ops_list: list[ast.expr] = []
    for item in migration_class.body:
        if isinstance(item, ast.Assign):
            for target in item.targets:
                if isinstance(target, ast.Name) and target.id == "operations":
                    if isinstance(item.value, ast.List):
                        ops_list = item.value.elts

    app_label = path.parent.parent.name
    migration_name = f"{app_label}.{path.stem}"
    migration_stem = path.stem

    # Operations that mrt check may flag but mrt fix cannot auto-resolve.
    # Only truly irreversible variants are listed — e.g. AddField(null=True) is
    # fine, but AddField NOT NULL without a default is flagged by mrt check and
    # cannot be auto-fixed. We detect these by name only (the detector handles
    # the nuance); here we just surface them to the user.
    _UNSUPPORTED_OPS = {"AlterField", "RenameField", "RenameModel"}

    patches: list[DjangoOpPatch] = []
    unsupported: list[str] = []

    for op in ops_list:
        if not isinstance(op, ast.Call):
            continue
        name = _op_name(op)

        patch: DjangoOpPatch | None = None
        if name == "RunSQL":
            patch = _patch_run_sql(source, op)
        elif name == "RunPython":
            patch = _patch_run_python(source, op)
        elif name == "RemoveField":
            patch = _patch_remove_field(source, op, app_label, migration_stem)
        elif name == "DeleteModel":
            patch = _patch_delete_model(source, op, app_label, migration_stem)
        elif name == "AddField":
            # Only flag AddField that is NOT NULL with no default — same condition
            # that mrt check (MRT411) flags. null=True or a default= means it's safe.
            if _add_field_is_problematic(op):
                unsupported.append("AddField NOT NULL without default")
        elif name in _UNSUPPORTED_OPS:
            unsupported.append(name)

        if patch is not None:
            patches.append(patch)

    if not patches:
        # Return a suggestion with no patches but unsupported ops listed,
        # so the caller can show a meaningful message instead of "no fix needed".
        if unsupported:
            return DjangoFixSuggestion(
                file=path.name,
                migration_name=migration_name,
                patches=[],
                patched_source=source,
                confidence="none",
                unsupported_ops=unsupported,
            )
        return None

    patched_source = _apply_patches_to_source(source, patches)

    confidences = {p.confidence for p in patches}
    overall = "low" if "low" in confidences else ("medium" if "medium" in confidences else "high")

    warnings: list[str] = []
    if any(p.op_name == "RunPython" for p in patches):
        warnings.append(
            "RunPython reverse set to migrations.RunPython.noop — does NOT undo "
            "data changes. Replace noop with a real reverse function."
        )
    if any(p.op_name in ("RemoveField", "DeleteModel") for p in patches):
        warnings.append(
            "Backup/restore code injected. Run `mrt clean-backups --db URL` "
            "after confirming stable deployment to remove backup data."
        )

    return DjangoFixSuggestion(
        file=path.name,
        migration_name=migration_name,
        patches=patches,
        patched_source=patched_source,
        confidence=overall,
        warning="\n\n".join(warnings) if warnings else None,
    )


def apply_django_fix(migration_path: str, fix: DjangoFixSuggestion) -> None:
    """Write the patched source to the migration file."""
    Path(migration_path).write_text(fix.patched_source)
